public class Animais{

    public String reino;
    public String nome;
    public Animais(String reino, String nome) {

        this.reino = reino;
        this.nome = nome;
    }
    public String getReino() {
        return reino;
    }
    public void setReino(String reino) {
        this.reino = reino;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
}
	
	