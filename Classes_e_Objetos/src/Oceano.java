public class Oceano{
    public String tipo;
    public Double tamanho;
    public Oceano(String tipo, Double tamanho) {

        this.tipo = tipo;
        this.tamanho = tamanho;
    }
    public String getTipo() {
        return tipo;
    }
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    public Double getTamanho() {
        return tamanho;
    }
    public void setTamanho(Double tamanho) {
        this.tamanho = tamanho;
    }}